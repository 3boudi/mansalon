/* --- Translation Engine --- */
function setLanguage(lang) {
  const t = translations[lang];
  if (!t) return;

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key]) el.textContent = t[key];
  });

  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const key = el.getAttribute('data-i18n-html');
    if (t[key]) el.innerHTML = t[key];
  });

  const htmlEl = document.documentElement;
  if (lang === 'ar') {
    htmlEl.setAttribute('dir', 'rtl');
    htmlEl.setAttribute('lang', 'ar');
    document.body.classList.add('lang-ar');
    document.body.classList.remove('lang-fr');
  } else if (lang === 'fr') {
    htmlEl.setAttribute('dir', 'ltr');
    htmlEl.setAttribute('lang', 'fr');
    document.body.classList.remove('lang-ar');
    document.body.classList.add('lang-fr');
  } else {
    htmlEl.setAttribute('dir', 'ltr');
    htmlEl.setAttribute('lang', 'en');
    document.body.classList.remove('lang-ar', 'lang-fr');
  }

  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.getAttribute('data-lang') === lang);
  });

  localStorage.setItem('preferred-lang-homme', lang);
}

document.addEventListener('DOMContentLoaded', () => {
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.addEventListener('click', () => {
      setLanguage(btn.getAttribute('data-lang'));
    });
  });

  const savedLang = localStorage.getItem('preferred-lang-homme') || 'en';
  setLanguage(savedLang);
});
